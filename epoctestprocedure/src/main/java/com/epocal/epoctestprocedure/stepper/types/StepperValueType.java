package com.epocal.epoctestprocedure.stepper.types;

/**
 * Represents the values of the Stepper Type. E.g, for TextBoxes, Dates etc the value type is VT_STRING
 * while RADIO/COMBO boxes supply an array of data plus an index on which one should be selected.
 */
public enum StepperValueType {
    VT_STRING, VT_ARRAY
}
